CREATE DATABASE VENDASWK;
USE VENDASWK;

CREATE TABLE CLIENTES (
    CODIGO INT NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(100),
    CIDADE VARCHAR(100),
    UF CHAR(2),
    PRIMARY KEY (CODIGO)
);

CREATE TABLE PRODUTOS (
    CODIGO INT NOT NULL AUTO_INCREMENT,
    DESCRICAO VARCHAR(255),
    PRECOVENDA DECIMAL(10, 2),
    PRIMARY KEY (CODIGO)
);

CREATE TABLE PEDIDOSGERAIS (
    NUMEROPEDIDO INT NOT NULL AUTO_INCREMENT,
    DATAEMISSAO TIMESTAMP,
    CODIGOCLIENTE_ID INT,
    VALORTOTAL DECIMAL(10, 2),
    PRIMARY KEY (NUMEROPEDIDO),
    FOREIGN KEY (CODIGOCLIENTE_ID) REFERENCES CLIENTES(CODIGO)
);

CREATE TABLE PEDIDOPRODUTOS (
    ID INT NOT NULL AUTO_INCREMENT,
    NUMEROPEDIDO_ID INT,
    CODIGOPRODUTO INT,
    QUANTIDADE INT,
    VLRUNITARIO DECIMAL(10, 2),
    VLRTOTAL DECIMAL(10, 2),
    DESCRICAO VARCHAR(100),
    PRIMARY KEY (ID),
    FOREIGN KEY (NUMEROPEDIDO_ID) REFERENCES PEDIDOSGERAIS(NUMEROPEDIDO),
    FOREIGN KEY (CODIGOPRODUTO) REFERENCES PRODUTOS(CODIGO)
);

